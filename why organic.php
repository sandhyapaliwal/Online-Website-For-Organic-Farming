  <h2 class=" bg-green text-blue text-center"> Introduction of Organic Farming </h2> 
  <p class="py-3"> Organic farming is a production system which avoids or largely excludes the use of synthetically compounded fertilizers, pesticides, growth regulators, genetically modified organisms and livestock food additives. To the maximum extent possible organic farming system rely upon crop rotations, use of crop residues, animal manures, legumes, green manures, off farm organic wastes, biofertilizers, mechanical cultivation, mineral bearing rocks and aspects of biological control to maintain soil productivity and tilth to supply plant nutrients and to control insect, weeds and other pests.Organic methods can increase farm productivity, repair decades of environmental damage and knit small farm families into more sustainable distribution networks leading to improved food security if they organize themselves in production, certification and marketing. During last few years an increasing number of farmers have shown lack of interest in farming and the people who used to cultivate are migrating to other areas. Organic farming is one way to promote either self-sufficiency or food security. Use of massive inputs of chemical fertilizers and toxic pesticides poisons the land and water heavily. The after-effects of this are severe environmental consequences, including loss of topsoil, decrease in soil fertility, surface and ground water contamination and loss of genetic diversity.Organic farming which is a holistic production management system that promotes and enhances agro-ecosystem health, including biodiversity, biological cycles, and soil biological activity is hence important. Many studies have shown that organic farming methods can produce even higher yields than conventional methods. Significant difference in soil health indicators such as nitrogen mineralization potential and microbial abundance and diversity, which were higher in the organic farms can also be seen. The increased soil health in organic farms also resulted in considerably lower insect and disease incidence. The emphasis on small-scale integrated farming systems has the potential to revitalize rural areas and their economies. 

    <h2 class=" bg-green text-blue text-center"> Advantages of Organic Farming </h2>
    1. It helps to maintain environment health by reducing the level of pollution. 

    2. It reduces human and animal health hazards by reducing the level of residues in the product. 

    3. It helps in keeping agricultural production at a sustainable level. 

    4. It reduces the cost of agricultural production and also improves the soil health. 

    5. It ensures optimum utilization of natural resources for short-term benefit and helps in conserving them for future generation. 

    6. It not only saves energy for both animal and machine, but also reduces risk of crop failure. 

    7. It improves the soil physical properties such as granulation, good tilth, good aeration, easy root penetration and improves water-holding capacity and reduces erosion. 

    8. It improves the soil’s chemical properties such as supply and retention of soil nutrients, reduces nutrient loss into water bodies and environment and promotes favourable chemical reactions.  


    <h2 class=" bg-green text-blue text-center"> Nutrients Mangement in Organic Farming </h2>
  In organic farming, it is important to constantly work to build a healthy soil that is rich in organic matter and has all the nutrients that the plants need. Several methods viz. green manuring, addition of manures and biofertilizers etc can be used to build up soil fertility. These organic sources not only add different nutrients to the soil but also help to prevent weeds and increase soil organic matter to feed soil microorganisms. Soil with high organic matter resists soil erosion, holds water better and thus requires less irrigation. Some natural minerals that are needed by the plants to grow and to improve the soil’s consistency can also be added. Soil amendments like lime are added to adjust the soil’s pH balance. However soil amendment and water should contain minimum heavy metals. Most of the organic fertilizers used are recycled by-products from other industries that would otherwise go to waste. Farmers also make compost from animal manures and mushroom compost. Before compost can be applied to the fields, it is heated and aged for at least two months, reaching and maintaining an internal temperature of 130°-140°F to kill unwanted bacteria and weed seeds. A number of organic fertilizers / amendments and bacterial and fungal biofertilizers can be used in organic farming depending upon availability and their suitability to crop. </p>  
